package com.TeamNovus.SupernaturalRaces.Models;

public class Player {
	private String race;
	private Integer power;
	
	public String getRace() {
		return race;
	}
	
	public void setRace(String race) {
		this.race = race;
	}

	public Integer getPower() {
		return power;
	}

	public void setPower(Integer power) {
		this.power = power;
	}
}
